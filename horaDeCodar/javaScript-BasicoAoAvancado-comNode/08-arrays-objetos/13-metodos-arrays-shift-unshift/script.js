// shift - inserir elemento do array (no inicio do array)
// unshift - remove elemento do array (no inicio do array)

let carros = ["BMW", "Audi", "VW", "Fiat"]

let removerPrimeirocarro = carros.shift()

console.log(removerPrimeirocarro)
console.log(carros)

carros.unshift("Ferrari")
console.log(carros)


